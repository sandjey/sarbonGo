// Msg returns localized API description for key. Fallback: en -> ru -> key.
func Msg(key, lang string) string {
	lang = strings.ToLower(strings.TrimSpace(lang))
	m, ok := apiMessages[key]
	if !ok {
		return key
	}
	if s, ok := m[lang]; ok && s != "" {
		return s
	}
	if s, ok := m["en"]; ok && s != "" {
		return s
	}
	if s, ok := m["ru"]; ok && s != "" {
		return s
	}
	return key
}

// LangFromContext returns X-Language from gin.Context; default "en".
func LangFromContext(c interface{ GetHeader(string) string }) string {
	lang := strings.ToLower(strings.TrimSpace(c.GetHeader("X-Language")))
	switch lang {
	case "ru", "uz", "en", "tr", "zh":
		return lang
	}
	return "en"
}
